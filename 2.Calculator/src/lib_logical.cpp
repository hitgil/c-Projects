#include "../inc/calci.h"

float Bitwise_OR(int number1, int number2)
{
    return (number1 | number2);
}

float Bitwise_AND(int number1, int number2)
{
    return (number1 & number2);
}

float Bitwise_NOT(int number1)
{
    return ~number1;
}

float Bitwise_XOR(int number1, int number2)
{
    return (number1 ^ number2);
}
